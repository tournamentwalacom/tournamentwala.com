-- Storage bucket for organizer-uploaded posters. Public so the site can
-- render the image via its public URL with no signed-URL dance; uploads
-- only ever happen server-side (service_role, via /api/upload/poster),
-- so no public storage.objects policies are needed.
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'tournament-posters',
  'tournament-posters',
  true,
  8388608,
  array['image/jpeg', 'image/png', 'image/webp']
)
on conflict (id) do update set
  public = excluded.public,
  file_size_limit = excluded.file_size_limit,
  allowed_mime_types = excluded.allowed_mime_types;

-- Public site (anon key) can only read tournaments marked "live".
-- Nothing else is exposed to anon — inserts happen through the
-- /api/tournaments/submit route using the service_role key, which forces
-- status = 'pending' server-side regardless of what the client sends.
-- The admin panel also uses the service_role key, which bypasses RLS entirely.
-- Dropped-then-recreated so this whole file stays safe to run more than once.
drop policy if exists "Public can read live tournaments" on tournaments;
create policy "Public can read live tournaments"
  on tournaments for select
  using (status = 'live');

-- Promotion add-ons organizers can buy when submitting a tournament (Instagram
-- post/story/reel, HOT badge, announcement bar, poster/reel design, etc).
-- Admin-managed via /admin/pricing; the public form fetches active rows
-- through /api/promotion-packages so prices never live in frontend JS.
create table if not exists promotion_packages (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  description text,
  price numeric not null default 0,
  price_unit text,
  allow_quantity boolean not null default false,
  requires_telegram_upload boolean not null default false,
  is_free boolean not null default false,
  is_active boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table promotion_packages enable row level security;

