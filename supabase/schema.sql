-- column was never created in the first place.
do $$
begin
  if exists (
    select 1 from information_schema.columns
    where table_name = 'tournaments' and column_name = 'organizer_email'
  ) then
    alter table tournaments alter column organizer_email drop not null;
  end if;
end $$;

